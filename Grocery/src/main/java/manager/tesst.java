package manager;

public class tesst {

	private String name;
	private String image;
	
	public tesst(String name, String image) {
		
		this.name = name;
		this.image = image;
	}
	
	public tesst() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
	
	
}
